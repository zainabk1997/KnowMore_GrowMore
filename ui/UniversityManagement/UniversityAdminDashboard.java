    private void populateMealPlanAdminTable() {
        DefaultTableModel model = (DefaultTableModel) tblMealSupplier.getModel();
        model.setRowCount(0);
        for (PersonU admin : ecoSystem.getPersonDirectoryRef().getPersonDirectory()) {
            PersonU.UserRole u = admin.getUserRole();
            if (String.valueOf(u).equals("MEALPLAN_ADMIN")) {
                Object[] row = new Object[3];
                row[0] = admin.getPersonName();
                row[1] = admin.getCommunity().getCommunity();
                row[2] = admin.getCommunity().getCity().getCity();
                model.addRow(row);
            }
        }
    }

    private void populateStudentMealPlanTable() {
        DefaultTableModel model = (DefaultTableModel) tblStudentMeal.getModel();
        model.setRowCount(0);
        for(StudentMealPlan studentMealPlan : ecoSystem.getStudentMealPlan().getStudentMealPlanDirectory()){
            String studentID = studentMealPlan.getStudentID();
            StudentU student = ecoSystem.getStudentDirectoryRef().getStudentByID(studentID);
            String studentName = student.getPersonName();
            String studentDegreeLevel = student.getStudentDegree().getDegreeLevel();
            int mealID = studentMealPlan.getMealPlanID();
            MealPlan meal = ecoSystem.getMealPlanDirectoryRef().getPlanByCode(mealID);
            Object[] row = new Object[4];
            row[0] = studentID;
            row[1] = studentName;
            row[2] = studentDegreeLevel;
            row[3] = String.valueOf(mealID);
            model.addRow(row);
            
        }

    }

